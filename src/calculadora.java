    private void jToggleButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton7ActionPerformed
        boolean teste = true;
        
        if(textoCalc.contains("+")||textoCalc.contains("-")||textoCalc.contains("/")||textoCalc.contains("*")){        
            if(textoCalc.endsWith("+")||textoCalc.endsWith("-")||textoCalc.endsWith("/")||textoCalc.endsWith("*")){
                apagarBackSpace();                
            }
            while(teste == true && textoCalc.length() > 0){
                if(textoCalc.endsWith(".") == true){
                    ponto = true;
                }
                if(textoCalc.endsWith("+")||textoCalc.endsWith("-")||textoCalc.endsWith("/")||textoCalc.endsWith("*")){
                    apagarBackSpace();
                    teste = false;
                    if(textoCalc.endsWith("+")||textoCalc.endsWith("-")||textoCalc.endsWith("/")||textoCalc.endsWith("*")){
                        apagarBackSpace();
                        if(textoCalc.startsWith("(") && textoCalc.endsWith(")")){
                            //textoCalc = textoCalc.substring(1,textoCalc.length()-1);
                            apagarBackSpace();
                        }
                    }
                }else{
                    apagarBackSpace();
                }
            }
        }else{        
            textoCalc = "0";
            ponto = true;
        }
        
        if(textoCalc.length() == 0){
            textoCalc = "0";
            ponto = true;
        }
        if(textoCalc.endsWith("+")||textoCalc.endsWith("-")||textoCalc.endsWith("/")||textoCalc.endsWith("*")){
            calculo = false;
            calculo1 = false;
        }else{            
            calculo = true;
            calculo1 = true;
        }
        
        jTextField1.setText(textoCalc);
    }//GEN-LAST:event_jToggleButton7ActionPerformed

