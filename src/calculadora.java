    private void teclarNumero(String numero){
    if(textoCalc.length() == 1 && textoCalc == "0"){
        textoCalc = "";                
    }    
    textoCalc += numero;
        calculo = true;
        calculo1 = true;
        jTextField1.setText(textoCalc);
    }
    
    private void apagarBackSpace(){
        if(textoCalc.endsWith(".") == true){
            ponto = true;
        }
        if(textoCalc.startsWith("(") && textoCalc.endsWith(")")){
            textoCalc = textoCalc.substring(1,textoCalc.length());
        }
        textoCalc = textoCalc.substring(0,textoCalc.length()-1);     
        if(textoCalc.length() == 0){
            textoCalc = "0";
            ponto = true;
        }
        
        if(textoCalc.endsWith("+")||textoCalc.endsWith("-")||textoCalc.endsWith("/")||textoCalc.endsWith("*")){
            calculo = false;
            calculo1 = false;          
            
        }else{            
            calculo = true;
            calculo1 = true;
        }
        if(textoCalc.length() == 1 && textoCalc == "0"){
            ponto = true;
        }
        
        jTextField1.setText(textoCalc);
    }
    
