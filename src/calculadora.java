    private void jToggleButton19ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton19ActionPerformed
    ScriptEngineManager mgr = new ScriptEngineManager();
    ScriptEngine engine = mgr.getEngineByName("JavaScript");    
    
    if(textoCalc.endsWith("+")||textoCalc.endsWith("-")||textoCalc.endsWith("/")||textoCalc.endsWith("*")){
        apagarBackSpace();
        calculo = true;
        calculo1 = true;
    }
    
    if(textoCalc.endsWith(".")){
        apagarBackSpace();
        ponto = true;
    }    
    String contaString = textoCalc;    
    try {
        String text = engine.eval(contaString).toString();
        textoCalc = text;            
    }catch (ScriptException ex) {               
            Logger.getLogger(calculadora.class.getName()).log(Level.SEVERE, null, ex);
    }
    //if(textoCalc.endsWith(".0")){
        //apagarBackSpace();
        //apagarBackSpace();
    //}
    jTextField1.setText(textoCalc);              
    }//GEN-LAST:event_jToggleButton19ActionPerformed

